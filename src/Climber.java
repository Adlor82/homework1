public class Climber {

    private String name;
    private int climberPower;

    public Climber(String name, int climberPower) {
        this.name = name;
        this.climberPower = climberPower;
    }

    public String getName() {
        return name;
    }
    public int getClimberPower() {
        return climberPower;
    }
}
